%通过两条线段四个端点求交点坐标
function joint=clcLineJoint(P1,P2,P3,P4)
    x1=P1(1,1);y1=P1(2,1);x2=P2(1,1);y2=P2(2,1);x3=P3(1,1);y3=P3(2,1);x4=P4(1,1);y4=P4(2,1);
    x=((y3-y1)*(x2-x1)*(x4-x3)+x1*(x4-x3)*(y2-y1)-x3*(x2-x1)*(y4-y3))/((x4-x3)*(y2-y1)-(x2-x1)*(y4-y3));
    y=((y2-y1)*(y4-y3)*(x1-x3)+y3*(y2-y1)*(x4-x3)-y1*(y4-y3)*(x2-x1))/((y2-y1)*(x4-x3)-(y4-y3)*(x2-x1));
    joint(1,1)=x;
    joint(2,1)=y;
end 