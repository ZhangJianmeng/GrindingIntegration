function [b1,b2,b3,b4,b5,b6,b7,b8,b9,...
          b10,b11,b12,b13,b14,b15,b16,b17,b18,b19,b20,a] = calcCoeffB20(obj,t)

    [a1,a2,a3,a4,a5,a6,a7,a8,a9,a10] = calcCoeffA10(obj,t);
    
    M = obj.calcTransMatrix(t);

    M32 = M(3,2); M33 = M(3,3); M34 = M(3,4);

    z0 = obj.workpiece.zOmega;
    R1 = obj.cutter.R1;
    R2 = obj.cutter.R2;
    %º∆À„b11-b20
    b1 = -(a3^2*M32^2) - a5^2*M33^2*R2^2;
    b2 = -2*a1*a8*M32^2 + 2*a7^2*M32^2*R1*R2;
    b3 = (-2*a2*a3 - 2*a1*a4)*M32^2 + 2*a6^2*M32^2*R1*R2 + 2*a5*a6*M32^2*R2^2;
    b4 = (-2*a1*a10 - 2*a2*a9)*M32^2 + 4*a6*a7*M32^2*R1*R2 + 2*a5*a7*M32^2*R2^2;
    
    b5 = -2*a2*a4*M32^2 + a6^2*M32^2*R2^2;
    b6 = -2*a10*a2*M32^2 + 2*a6*a7*M32^2*R2^2;
    b7 = (-a1^2 - 2*a2*a8)*M32^2 + a7^2*M32^2*R2^2;
    
    b8 = -2*a1*a2*M32^2;
    
    b9 = -a2^2*M32^2;
    
    b10 = -2*a3*a9*M32^2 + 2*a5^2*M33^2*R2^2 + R2*(2*a5^2*M33*M34 - 2*a5^2*M33*z0);
    
    b11 = -(a9^2*M32^2) - a5^2*M34^2 + a5^2*M32^2*R1^2 - a5^2*M33^2*R2^2 + ...
          2*a5^2*M34*z0 - a5^2*z0^2 + R2*(-2*a5^2*M33*M34 + 2*a5^2*M33*z0);
    
    b12 = -2*a3*a4*M32^2 - 2*a5*a6*M33^2*R2^2;
    b13 = (-2*a10*a3 - 2*a4*a9)*M32^2 + (4*a5*a6*M33^2 - 2*a5*a7*M33^2)*R2^2 + R2*(4*a5*a6*M33*M34 - 4*a5*a6*M33*z0);
    b14 = -2*a8*a9*M32^2 - 2*a5*a7*M34^2 + 2*a5*a7*M32^2*R1^2 - 2*a5*a7*M33^2*R2^2 + 4*a5*a7*M34*z0 - ...
          2*a5*a7*z0^2 + R2*(-4*a5*a7*M33*M34 + 4*a5*a7*M33*z0);
    b15 = (-2*a3*a8 - 2*a10*a9)*M32^2 - 2*a5*a6*M34^2 + 2*a5*a6*M32^2*R1^2 + 2*a5^2*M32^2*R1*R2 + ...
          (-2*a5*a6*M33^2 + 4*a5*a7*M33^2)*R2^2 + 4*a5*a6*M34*z0 - 2*a5*a6*z0^2 + ...
           R2*(-4*a5*a6*M33*M34 + 4*a5*a7*M33*M34 + 4*a5*a6*M33*z0 - 4*a5*a7*M33*z0);
         
    b16 = -a8^2*M32^2 - a7^2*M34^2 + a7^2*M32^2*R1^2 - a7^2*M33^2*R2^2 + 2*a7^2*M34*z0 - ...
          a7^2*z0^2 + R2*(-2*a7^2*M33*M34 + 2*a7^2*M33*z0);
    b17 = -(a4^2*M32^2) - a6^2*M33^2*R2^2;
    b18 = -2*a10*a4*M32^2 + (2*a6^2*M33^2 - 2*a6*a7*M33^2)*R2^2 + R2*(2*a6^2*M33*M34 - 2*a6^2*M33*z0);
    b19 = (-a10^2 - 2*a1*a3 - 2*a4*a8)*M32^2 - a6^2*M34^2 + a6^2*M32^2*R1^2 + 4*a5*a6*M32^2*R1*R2 + ...
          (a5^2*M32^2 - a6^2*M33^2 + 4*a6*a7*M33^2 - a7^2*M33^2)*R2^2 + 2*a6^2*M34*z0 - a6^2*z0^2 + ...
          R2*(-2*a6^2*M33*M34 + 4*a6*a7*M33*M34 + 2*a6^2*M33*z0 - 4*a6*a7*M33*z0);
    b20 = (-2*a10*a8 - 2*a1*a9)*M32^2 - 2*a6*a7*M34^2 + 2*a6*a7*M32^2*R1^2 + 4*a5*a7*M32^2*R1*R2 + ...
          (-2*a6*a7*M33^2 + 2*a7^2*M33^2)*R2^2 + 4*a6*a7*M34*z0 - 2*a6*a7*z0^2 + ...
          R2*(-4*a6*a7*M33*M34 + 2*a7^2*M33*M34 + 4*a6*a7*M33*z0 - 2*a7^2*M33*z0);
    
    a = [a1,a2,a3,a4,a5,a6,a7,a8,a9,a10];
end

